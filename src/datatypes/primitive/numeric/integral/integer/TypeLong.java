package datatypes.primitive.numeric.integral.integer;

public class TypeLong {
    public static void main(String[] args) {
        System.out.println(Long.MIN_VALUE);
        // -9223372036854775808
        System.out.println(Long.MAX_VALUE);
        // 9223372036854775807

        long l = 25L;
        System.out.println(l);
    }
}
